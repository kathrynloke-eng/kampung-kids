export function t(
  locale: Locale,
  key: UiKey,
  vars?: Record<string, string | number>,
): string {
  const template = ui[locale][key] ?? ui.en[key];
  if (!vars) return template;
  let text: string = template;
  for (const [k, v] of Object.entries(vars)) {
    text = text.replaceAll(`{${k}}`, String(v));
  }
  return text;
}
